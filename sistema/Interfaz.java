    public void cargardatos(Datos dat){
        this.tapellidos.setText(dat.getApellidos());
        this.tcodigo.setText(dat.getCod());
        this.tdireccion.setText(dat.getDir());
        this.tentidad.setText(dat.getEntidad());
        this.tfecha.setText(dat.getFecha());
        this.tlugar.setText(dat.getLugar());
        this.tmedico.setText(dat.getMedico());
        this.tmotivo.setText(dat.getMotivo());
        this.tnombres.setText(dat.getNombres());
        this.tsexo.setText(dat.getSexo());
        
    }
